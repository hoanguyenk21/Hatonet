                <div class="job-item__image" v-if="job.user">
                  <SectionImage class="img" :src="baseUrl + job.user.avatar" />
                </div>
              </nuxt-link>
              <div class="job-item__content">
                <nuxt-link class="title" :to="{ path: `/jobs/${job.id}` }">
                  {{ job.title }}
                </nuxt-link>
                <ul class="tags">
                  <li>
                    <div v-for=" proMe in job.product_meta" :key="proMe.id">
                      <div v-if="proMe.meta_key == '_skill'">
                        <div v-for="skill in options.skill">
                          <span v-if="proMe.meta_value == skill.id">
                            {{ skill.name }}
                          </span>
                        </div>
                      </div>
                    </div>
                  </li>
                </ul>
                <ul class="meta">
                  <li>
                    <i class="ti ti-building-skyscraper" v-if="job.user"><span>{{ job.user.name }}</span></i>
                  </li>
                  <li v-for="proMeta in job.product_meta" :key="'job_address' + proMeta.id"
                    v-if="proMeta.meta_key == '_address'">
                    <i class="ti ti-map-pin">{{ options.location[+proMeta.meta_value].name }}</i>
                  </li>
                  <li v-for="proMeta in job.product_meta" :key="'job_address' + proMeta.id"
                    v-if="proMeta.meta_key == '_dateline'">
                    <i class="ti ti-calendar-event">{{ getDate(proMeta.meta_value) }}</i>
                  </li>
                </ul>
              </div>
              <div class="job-item__redirect">
                <div class="price">
                  <small>Giá thuê</small>
                  <span>{{
                      new Intl.NumberFormat().format(job.price_sale)
                  }}
                    VND</span>
                </div>
                <nuxt-link class="title" :to="{ path: `/jobs/${job.id}` }">
                  <i class="ti ti-arrow-right"></i>
                </nuxt-link>
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
    <div class="block__bottom">
      <div class="load-more">
        <button v-if="jobSeeMore.length == jobs.length && jobs.length >= 7" class="d-flex align-items-center"
          @click="collapseTo">
          Thu gọn
          <i class="ti ti-arrow-up-circle"></i>
        </button>
        <span v-else>
          <button v-if="jobs.length > this.length" @click="companies">
            <span>Xem thêm</span>
            <span><svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor"
                class="bi bi-arrow-down-circle" viewBox="0 0 16 16">
                <path fill-rule="evenodd"
                  d="M1 8a7 7 0 1 0 14 0A7 7 0 0 0 1 8zm15 0A8 8 0 1 1 0 8a8 8 0 0 1 16 0zM8.5 4.5a.5.5 0 0 0-1 0v5.793L5.354 8.146a.5.5 0 1 0-.708.708l3 3a.5.5 0 0 0 .708 0l3-3a.5.5 0 0 0-.708-.708L8.5 10.293V4.5z" />
              </svg></span>
          </button>
        </span>
      </div>
    </div>
  </div>
</template>

<script>
import LoadMore from '@/components/LoadMore.vue'
import { baseUrl } from '@/env/env.development.js'
import SectionImage from '../../components/client/SectionImage.vue'
import { LOCATION, SKILL, STATUS } from '../../constants/common'
export default {
  components: { LoadMore, SectionImage },
  props: ['jobs'],
  data: () => ({
    options: {
      status: STATUS,
      location: LOCATION,
      skill: SKILL,
    },
    baseUrl: baseUrl,
    length: 6,
  }),
  computed: {
    jobSeeMore() {
      return this.jobs.slice(0, this.length);
    },
  },
  methods: {
    getDate(value) {
      if (typeof value === 'string') {

        return value.replace(/["\\]/g, '');
      }
      return value;
    },
    companies() {
      if (this.length > this.jobs.length) return
      this.length = this.length + 6
    },
    collapseTo() {
      this.length = 6
    },
  },
}
</script>

